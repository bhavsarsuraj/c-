#include <iostream>

using namespace std;

int main() {
    
    int resOfAnd = 15 & 30;
    int resOfOr = 15 | 30;
    int resOfNot = ~ 15;
    int resOfXor = 15 ^ 40;
    int resOfLeftShift = 15 << 2;
    int resOfRightShift = 15 >> 2;
    
    cout<<resOfAnd<<" "<<resOfOr<<" "<<resOfNot<<" "<<resOfXor<<" "<<resOfLeftShift<<" "<<resOfRightShift<<endl;
    
    
    
}
